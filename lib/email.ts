import { Resend } from 'resend';
import nodemailer from 'nodemailer';
import dbConnect from '@/lib/db';
import Expense from '@/models/Expense';
import Employee from '@/models/Employee';
import { formatCurrency, formatDate, startOfDayUTC, endOfDayUTC, startOfMonthUTC, endOfMonthUTC } from '@/lib/utils';

const resend = process.env.RESEND_API_KEY ? new Resend(process.env.RESEND_API_KEY) : null;
const REPORT_EMAIL = process.env.REPORT_EMAIL_TO || 'admin@moneyflow.com';
const FROM_EMAIL = 'Vision Glass & Interior <onboarding@resend.dev>';

// Nodemailer fallback transporter
function getNodemailerTransport() {
  return nodemailer.createTransport({
    host: process.env.SMTP_HOST || 'smtp.gmail.com',
    port: parseInt(process.env.SMTP_PORT || '587', 10),
    secure: false,
    auth: {
      user: process.env.SMTP_USER,
      pass: process.env.SMTP_PASS,
    },
  });
}

async function sendEmail(to: string, subject: string, html: string): Promise<boolean> {
  try {
    if (resend) {
      await resend.emails.send({
        from: FROM_EMAIL,
        to: [to],
        subject,
        html,
      });
      return true;
    }

    // Fallback to Nodemailer
    const transport = getNodemailerTransport();
    await transport.sendMail({
      from: FROM_EMAIL,
      to,
      subject,
      html,
    });
    return true;
  } catch (error) {
    console.error('Email send error:', error);
    return false;
  }
}

