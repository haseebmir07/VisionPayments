// ─── Daily Report ────────────────────────────────────────────

export async function sendDailyReport(date: Date): Promise<boolean> {
  await dbConnect();

  const dayStart = startOfDayUTC(date);
  const dayEnd = endOfDayUTC(date);

  const expenses = await Expense.find({
    date: { $gte: dayStart, $lte: dayEnd },
  })
    .populate('employee', 'name employeeId')
    .sort({ date: -1 })
    .lean();

  if (expenses.length === 0) {
    return true; // No expenses, skip sending
  }

  const total = expenses.reduce((sum, e) => sum + e.amount, 0);
  const cashTotal = expenses
    .filter((e) => e.paymentMode === 'cash')
    .reduce((sum, e) => sum + e.amount, 0);
  const upiTotal = expenses
    .filter((e) => e.paymentMode === 'upi')
    .reduce((sum, e) => sum + e.amount, 0);

  const dateStr = formatDate(date, 'dd MMM yyyy');

  const rows = expenses
    .map((e) => {
      const emp = e.employee as unknown as { name: string; employeeId: string };
      return `
      <tr>
        <td>${emp?.name || 'Unknown'}</td>
        <td>${formatCurrency(e.amount)}</td>
        <td><span class="badge badge-${e.paymentMode}">${e.paymentMode.toUpperCase()}</span></td>
        <td>${e.description}</td>
      </tr>
    `;
    })
    .join('');

  const content = `
    <h2 style="margin-top:0;color:#1e293b;">Daily Expense Report — ${dateStr}</h2>
    <div style="text-align:center;margin:24px 0;">
      <div class="stat-box">
        <div class="stat-value">${formatCurrency(cashTotal)}</div>
        <div class="stat-label">Cash</div>
      </div>
      <div class="stat-box">
        <div class="stat-value">${formatCurrency(upiTotal)}</div>
        <div class="stat-label">UPI</div>
      </div>
      <div class="stat-box">
        <div class="stat-value">${formatCurrency(total)}</div>
        <div class="stat-label">Total</div>
      </div>
    </div>
    <table>
      <thead>
        <tr>
          <th>Employee</th>
          <th>Amount</th>
          <th>Payment</th>
          <th>Description</th>
        </tr>
      </thead>
      <tbody>
        ${rows}
        <tr class="total-row">
          <td>Total (${expenses.length} expenses)</td>
          <td>${formatCurrency(total)}</td>
          <td></td>
          <td></td>
        </tr>
      </tbody>
    </table>
  `;

  return sendEmail(
    REPORT_EMAIL,
    `Daily Expense Report — ${dateStr}`,
    baseTemplate(`Daily Report — ${dateStr}`, content)
  );
}

// ─── Monthly Report ──────────────────────────────────────────

export async function sendMonthlyReport(month: Date): Promise<boolean> {
  await dbConnect();

  const monthStart = startOfMonthUTC(month);
  const monthEnd = endOfMonthUTC(month);

  // Previous month for comparison
  const prevMonthDate = new Date(month);
  prevMonthDate.setUTCMonth(prevMonthDate.getUTCMonth() - 1);
  const prevMonthStart = startOfMonthUTC(prevMonthDate);
  const prevMonthEnd = endOfMonthUTC(prevMonthDate);

  const [currentExpenses, prevExpenses] = await Promise.all([
    Expense.aggregate([
      { $match: { date: { $gte: monthStart, $lte: monthEnd } } },
      {
        $group: {
          _id: '$employee',
          total: { $sum: '$amount' },
          count: { $sum: 1 },
          cashTotal: {
            $sum: { $cond: [{ $eq: ['$paymentMode', 'cash'] }, '$amount', 0] },
          },
          upiTotal: {
            $sum: { $cond: [{ $eq: ['$paymentMode', 'upi'] }, '$amount', 0] },
          },
        },
      },
    ]),
    Expense.aggregate([
      { $match: { date: { $gte: prevMonthStart, $lte: prevMonthEnd } } },
      {
        $group: {
          _id: '$employee',
          total: { $sum: '$amount' },
        },
      },
    ]),
  ]);

  if (currentExpenses.length === 0) {
    return true;
  }

  // Get employee details
  const employeeIds = currentExpenses.map((e) => e._id);
  const employees = await Employee.find({ _id: { $in: employeeIds } })
    .select('name employeeId')
    .lean();

  const employeeMap = new Map(employees.map((e) => [e._id.toString(), e]));
  const prevMap = new Map(
    prevExpenses.map((e) => [e._id.toString(), e.total])
  );

  const grandTotal = currentExpenses.reduce((sum, e) => sum + e.total, 0);
  const prevGrandTotal = prevExpenses.reduce((sum, e) => sum + e.total, 0);
  const monthStr = formatDate(month, 'MMMM yyyy');

  const rows = currentExpenses
    .map((e) => {
      const emp = employeeMap.get(e._id.toString());
      const prevTotal = prevMap.get(e._id.toString()) || 0;
      const delta = e.total - prevTotal;
      const deltaClass = delta > 0 ? 'color:#ef4444' : delta < 0 ? 'color:#10b981' : '';

      return `
      <tr>
        <td>${emp?.name || 'Unknown'} (${emp?.employeeId || ''})</td>
        <td>${formatCurrency(e.total)}</td>
        <td>${formatCurrency(e.cashTotal)}</td>
        <td>${formatCurrency(e.upiTotal)}</td>
        <td>${e.count}</td>
        <td style="${deltaClass}">${delta > 0 ? '+' : ''}${formatCurrency(Math.abs(delta))}</td>
      </tr>
    `;
    })
    .join('');

  const content = `
    <h2 style="margin-top:0;color:#1e293b;">Monthly Summary — ${monthStr}</h2>
    <div style="text-align:center;margin:24px 0;">
      <div class="stat-box">
        <div class="stat-value">${formatCurrency(grandTotal)}</div>
        <div class="stat-label">This Month</div>
      </div>
      <div class="stat-box">
        <div class="stat-value">${formatCurrency(prevGrandTotal)}</div>
        <div class="stat-label">Last Month</div>
      </div>
    </div>
    <table>
      <thead>
        <tr>
          <th>Employee</th>
          <th>Total</th>
          <th>Cash</th>
          <th>UPI</th>
          <th>Count</th>
          <th>vs Last Month</th>
        </tr>
      </thead>
      <tbody>
        ${rows}
        <tr class="total-row">
          <td>Grand Total</td>
          <td>${formatCurrency(grandTotal)}</td>
          <td></td>
          <td></td>
          <td></td>
          <td></td>
        </tr>
      </tbody>
    </table>
  `;

  return sendEmail(
    REPORT_EMAIL,
    `Monthly Summary — ${monthStr}`,
    baseTemplate(`Monthly Summary — ${monthStr}`, content)
  );
}
